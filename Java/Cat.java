class Cat{
 
 private String name;
 private String color;

 public Cat(){
}

 public Cat(String x, String y){
    this.name = x;
    this.color = y;
}
 
 public String getName(){
      return name;
 } 
 
 public void setName(String a){
      this.name = a;
 }
 
 public String getColor(){
      return color;
 } 
 
 public void setColor(String color){
      this.color = color;
 }
 
}
